
package IntroduccionALaProgramacion.Tema4;


public class BucleWhile {
    
    public static void main(String[] args) {
        
        int numeroWhile = 0;
        
        while (numeroWhile < 3) {  
            
            numeroWhile++;
            System.out.println("El valor de numeroWhile es: " + numeroWhile);
            
        }
        
    }
    
}