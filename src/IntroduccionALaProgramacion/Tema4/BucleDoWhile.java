
package IntroduccionALaProgramacion.Tema4;


public class BucleDoWhile {
    
    public static void main(String[] args) {
        
        int numeroDoWhile = 6;
        
        do {  
            
            numeroDoWhile++;
            System.out.println("El valor de numeroWhile es: " + numeroDoWhile);
            
        } while (numeroDoWhile < 3);
    }
    
}